package com.yzx.domain;

public class User {
	String id;
	String username;
	String score;
	String sum;
	String remark;
	String oraltime;
	String survey1;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getSum() {
		return sum;
	}
	public void setSum(String sum) {
		this.sum = sum;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getOraltime() {
		return oraltime;
	}
	public void setOraltime(String oraltime) {
		this.oraltime = oraltime;
	}
	public String getSurvey1() {
		return survey1;
	}
	public void setSurvey1(String survey1) {
		this.survey1 = survey1;
	}
	
}
